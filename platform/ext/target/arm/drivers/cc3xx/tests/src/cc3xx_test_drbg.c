/* Test data are inspired from DRBGVS test framework */
#define DRBG_HASH_RANDOM_BITS   1024
#define DRBG_CTR_RANDOM_BITS    512

void drbg_hash_test(struct test_result_t *ret)
{
    cc3xx_err_t err;
    struct cc3xx_drbg_hash_state_t state;

    uint8_t readback[DRBG_HASH_RANDOM_BITS / 8];
    const uint8_t entropy[] = {
        0x63, 0x36, 0x33, 0x77, 0xe4, 0x1e, 0x86, 0x46, 0x8d, 0xeb,
        0x0a, 0xb4, 0xa8, 0xed, 0x68, 0x3f, 0x6a, 0x13, 0x4e, 0x47,
        0xe0, 0x14, 0xc7, 0000, 0x45, 0x4e, 0x81, 0xe9, 0x53, 0x58,
        0xa5, 0x69};
    const uint8_t nonce[] = {
        0x80, 0x8a, 0xa3, 0x8f, 0x2a, 0x72, 0xa6, 0x23, 0x59, 0x91,
        0x5a, 0x9f, 0x8a, 0x04, 0xca, 0x68};
    const uint8_t entropy_reseed[] = {
        0xe6, 0x2b, 0x8a, 0x8e, 0xe8, 0xf1, 0x41, 0xb6, 0x98, 0x05,
        0x66, 0xe3, 0xbf, 0xe3, 0xc0, 0x49, 0x03, 0xda, 0xd4, 0xac,
        0x2c, 0xdf, 0x9f, 0x22, 0x80, 0x01, 0x0a, 0x67, 0x39, 0xbc,
        0x83, 0xd3};
    const uint8_t expected[DRBG_HASH_RANDOM_BITS / 8] = {
        0x04, 0xee, 0xc6, 0x3b, 0xb2, 0x31, 0xdf, 0x2c, 0x63, 0x0a,
        0x1a, 0xfb, 0xe7, 0x24, 0x94, 0x9d, 0000, 0x5a, 0x58, 0x78,
        0x51, 0xe1, 0xaa, 0x79, 0x5e, 0x47, 0x73, 0x47, 0xc8, 0xb0,
        0x56, 0x62, 0x1c, 0x18, 0xbd, 0xdc, 0xdd, 0x8d, 0x99, 0xfc,
        0x5f, 0xc2, 0xb9, 0x20, 0x53, 0xd8, 0xcf, 0xac, 0xfb, 0x0b,
        0xb8, 0x83, 0x12, 0x05, 0xfa, 0xd1, 0xdd, 0xd6, 0xc0, 0x71,
        0x31, 0x8a, 0x60, 0x18, 0xf0, 0x3b, 0x73, 0xf5, 0xed, 0xe4,
        0xd4, 0xd0, 0x71, 0xf9, 0xde, 0x03, 0xfd, 0x7a, 0xea, 0x10,
        0x5d, 0x92, 0x99, 0xb8, 0xaf, 0x99, 0xaa, 0x07, 0x5b, 0xdb,
        0x4d, 0xb9, 0xaa, 0x28, 0xc1, 0x8d, 0x17, 0x4b, 0x56, 0xee,
        0x2a, 0x01, 0x4d, 0x09, 0x88, 0x96, 0xff, 0x22, 0x82, 0xc9,
        0x55, 0xa8, 0x19, 0x69, 0xe0, 0x69, 0xfa, 0x8c, 0xe0, 0x07,
        0xa1, 0x80, 0x18, 0x3a, 0x07, 0xdf, 0xae, 0x17};

    err = cc3xx_lowlevel_drbg_hash_init(&state,
                    (const uint8_t *)entropy, sizeof(entropy),
                    nonce, sizeof(nonce), NULL, 0);
    TEST_ASSERT(err == CC3XX_ERR_SUCCESS, "drbg hash init failed");

    err = cc3xx_lowlevel_drbg_hash_reseed(&state,
                    (const uint8_t *)entropy_reseed, sizeof(entropy), NULL, 0);
    TEST_ASSERT(err == CC3XX_ERR_SUCCESS, "drbg hash reseed failed");

    /* The DRBG requires the number of bits to generate, aligned to byte-sizes */
    err = cc3xx_lowlevel_drbg_hash_generate(&state,
                    DRBG_HASH_RANDOM_BITS, readback, NULL, 0);
    TEST_ASSERT(err == CC3XX_ERR_SUCCESS, "drbg hash generate failed");

    /* The DRBG requires the number of bits to generate, aligned to byte-sizes */
    err = cc3xx_lowlevel_drbg_hash_generate(&state,
                    DRBG_HASH_RANDOM_BITS, readback, NULL, 0);
    TEST_ASSERT(err == CC3XX_ERR_SUCCESS, "drbg hash generate failed");

    TEST_ASSERT(memcmp(readback, expected, DRBG_HASH_RANDOM_BITS / 8) == 0,
                    "random bits mismatch");

    ret->val = TEST_PASSED;
cleanup:
    cc3xx_lowlevel_drbg_hash_uninit(&state);

    return;
}

